/*
Table: CLINICAL_SAMPLE

- Contains every public and consortium release in main genie for
    just the data_clinical_sample.txt files
- Refreshed monthly by genie ingestion scripts in snowflake repo
*/
CREATE OR REPLACE TABLE GENIE_DEV.MAIN.CLINICAL_SAMPLE (
	PATIENT_ID VARCHAR(16777216),
	SAMPLE_ID VARCHAR(16777216),
	AGE_AT_SEQ_REPORT VARCHAR(16777216),
	ONCOTREE_CODE VARCHAR(16777216),
	SAMPLE_TYPE VARCHAR(16777216),
	SEQ_ASSAY_ID VARCHAR(16777216),
	CANCER_TYPE VARCHAR(16777216),
	CANCER_TYPE_DETAILED VARCHAR(16777216),
	SAMPLE_TYPE_DETAILED VARCHAR(16777216),
	SAMPLE_CLASS VARCHAR(16777216),
	RELEASE VARCHAR(16777216),
	RELEASE_TYPE VARCHAR(16777216),
	MAJOR_VERSION NUMBER(38,0),
	MINOR_VERSION NUMBER(38,0),
	INGESTED_AT TIMESTAMP_NTZ(9),
	AGE_AT_SEQ_REPORT_DAYS VARCHAR(16777216),
	SEQ_DATE FLOAT,
	SEQ_YEAR FLOAT
);
